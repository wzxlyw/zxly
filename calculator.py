vopros = input(" +,-? ")

# +

if vopros == "+":
	pervoe = int(input(" Enter first number: "))
	vtoroe = int(input(" Enter second number: "))
	c = pervoe + vtoroe
	print(" Answer: " + str(c))
	input("")

# -

if vopros == "-":
	pervoe = int(input(" Enter first number: "))
	vtoroe = int(input(" Enter second number: "))
	c = pervoe - vtoroe
	print(" Answer: " + str(c))
	input("")
